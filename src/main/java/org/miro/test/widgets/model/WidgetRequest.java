package org.miro.test.widgets.model;

public class WidgetRequest {
    private Long x;
    private Long y;
    private Long width;
    private Long height;
    private Long zIndex;

    public Long getX() {
        return x;
    }

    public Long getY() {
        return y;
    }

    public Long getWidth() {
        return width;
    }

    public Long getHeight() {
        return height;
    }

    public Long getzIndex() {
        return zIndex;
    }
}
